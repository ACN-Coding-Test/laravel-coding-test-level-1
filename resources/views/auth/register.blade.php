@section('scripts')
    <script type="text/javascript">
        $(document).ready(function () {
            $(document).on('click', '#register', function (e) {
                e.preventDefault();

                $(this).text('Sending..');

                const data = {
                    'name': $('#name').val(),
                    'email': $('#email').val(),
                    'password': $('#password').val(),
                    'password_confirmation': $('#password-confirm').val(),
                }

                $.ajax({
                    type: "POST",
                    url: "/api/v1/auth/register",
                    data: data,
                    dataType: "json",
                    success: function (response) {
                        $('#errors').html("").removeClass('alert alert-danger');
                        $('#success_message').addClass('alert alert-success').text('successfully registered');
                        $('#register').text('Save');

                        localStorage.setItem('api_token', response.data.token);

                        window.location.href = '/events';
                    },
                    error: function (error) {
                        $('#errors').html("").addClass('alert alert-danger');
                        $.each(error.responseJSON.errors, function (key, err_value) {
                            $('#errors').append('<li>' + err_value[0] + '</li>');
                        });
                        $('#register').text('Save');
                    }
                });
            });
        });

    </script>
@stop

